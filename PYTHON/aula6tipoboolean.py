# Tipo de dado bool (boolean)
# Ao questionar algo em programa,
# só existe duas respostas possíveis:
# Sim (True) ou não (False).
# Existe vários operadores para "questionar".
# Dentre eles o ==, que é um operador lógico que 
# questina se um valor é igual a outro

print(10 == 10) # sim => (true) (verdadeiro)
print(10 == 11) # não => False (falso)
print(type(True))
print(type(False))
print(type(10 == 10))
print(type(10 == 11))

