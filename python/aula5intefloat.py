#Tipos int e float

# int-> numero inteiro 
# o tipo int reprensenta qualquer número 
# positivo ou negativo. int sem sinal é considerado
#positivo.

print(11) # int
print(-11) # int
print(0)   #int


# float -> número componto flutuante.
# o tipo float representa qualquer número
# positivo ou negativo com ponto flutuante.

# float sem sinal é considerado positivo.

print(1.1, 10.11) #tipo float
print(0.0) #float
# A função type mostra o tipo que o Python
# inferiu ao valor . 

print(type("Matheus")       )
print(type("1")  , type(0.0)    )


